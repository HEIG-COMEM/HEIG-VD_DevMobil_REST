<script setup>
import AppProfilePicture from './AppProfilePicture.vue';
import { defineProps, computed, ref, onMounted } from 'vue';

const props = defineProps({
  publication: Object,
});

const formatedDate = computed(() => {
  const date = new Date(props.publication.createdAt);

  return date.toDateString() === new Date().toDateString() ? `Aujourd'hui à ${date.toLocaleTimeString()}` : `Il y a ${Math.floor(
      (new Date() - date) / (1000 * 60 * 60 * 24)
    )} jours à ${date.toLocaleTimeString()}`;
});

const locality = ref('');
const getFormatedLocation = () => {
  const lat = props.publication.location.coordinates[1];
  const long = props.publication.location.coordinates[0];
  const url = `https://api-bdc.net/data/reverse-geocode-client?latitude=${lat}&longitude=${long}&localityLanguage=fr`;

  fetch(url)
    .then((response) => response.json())
    .then((data) => {
      locality.value = data.locality;
    });
};

onMounted(() => {
  getFormatedLocation();
});

const showFrontCamera = ref(true);
const toggleCamera = () => {
  showFrontCamera.value = !showFrontCamera.value;
};

const bigCamera = computed(() => {
  return showFrontCamera.value
    ? props.publication.frontCamera.url
    : props.publication.backCamera.url;
});

const smallCamera = computed(() => {
  return showFrontCamera.value
    ? props.publication.backCamera.url
    : props.publication.frontCamera.url;
});

</script>

<template>
  <div class="p-1">
    <div class="flex flex-row content-center justify-between gap-2 p-2">
      <AppProfilePicture :photo="publication.user.photo" />
      <div class="flex-grow">
        <p class="text-sm font-bold">{{ publication.user.name }}</p>
        <p class="text-xs text-gray-500">{{ locality }}, {{ formatedDate }}</p>
      </div>
      <div>...</div>
    </div>
    <div class="relative">
      <div
        class="absolute ml-4 mt-4 h-48 cursor-pointer rounded-lg bg-white shadow-lg"
        @click="toggleCamera()"
      >
        <img
          class="h-full w-full rounded-lg object-cover border border-black"
          :src="smallCamera"
          alt="publication"
        />
      </div>
      <img class="w-full rounded-lg object-cover" :src="bigCamera" alt="publication" />
    </div>
  </div>
</template>

